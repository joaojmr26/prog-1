# Ler um valor inteiro e escrever se é positivo ou negativo (só para efeitos desse programa, considere o valor zero como positivo).

def main():
    a = int(input())
    if a>=0:
        print("POSITIVO")
    else:
        print("NEGATIVO")
    
    return 0
if __name__ == "__main__":
    main()
    